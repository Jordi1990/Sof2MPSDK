int GetIDForString ( stringID_table_t *table, const char *string )
{
	int	index = 0;

	while ( ( table[index].name != NULL ) &&
			( table[index].name[0] != 0 ) )
	{
		if ( !strcmp( table[index].name, string ) )
			return table[index].id;

		index++;
	}

	return -1;
}

